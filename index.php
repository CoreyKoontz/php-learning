// ---------------------------------------------------------- Variables
// Variables in php begin with '$':
$name = 'Corey';
$greeting = 'Hello';
echo "$greeting, $name"; // Must use double quotes when using multiple variables in a string.
echo "Hello, {$name}"; // Sometimes you will see the variable wrapped in braces, but it is not necessary
echo $greeting . ' ' . $name; // concatenation is with "." instead of "+" in php.



